
public class Furniture {
	private int chairs;
	private int tables;
	private int beds;
	
	public Furniture(int chairs, int tables, int beds) {
		this.chairs = chairs;
		this.tables = tables;
		this.beds = beds;
	}
	
	public int getChairs() {
		return chairs;
	}
	
	public int getTables() {
		return tables;
	}
	
	public int getBeds() {
		return beds;
	}
}
