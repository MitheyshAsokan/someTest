
public class Bathroom {
	private String tub;
	private String comed;
	private int mirrors;
	
	public Bathroom(String tub, String comed, int mirrors) {
		this.tub = tub;
		this.comed = comed;
		this.mirrors = mirrors;
	}

	public String getTub() {
		return tub;
	}

	public String getComed() {
		return comed;
	}

	public int getMirrors() {
		return mirrors;
	}
	
	
	
}
